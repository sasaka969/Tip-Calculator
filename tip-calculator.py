print("Welcome to tip calculater🙏")

# asigning the values

bill = float(input("Bill: $"))
tip = int(input("Tip:"))
people = int(input("No. of people:"))

# calculation part 

amount = Bill * (1 + Tip / 100) / People

# final print statement

print(f"Each person should pay💸${amount:.2f}")
